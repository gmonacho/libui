#include <stdlib.h>

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	len;

	i = 0;
	while (s1[i])
	{
		i++;
	}
	len = 0;
	while (*s2 != '\0' && len < n)
	{
		s1[i++] = *s2++;
		len++;
	}
	s1[i] = '\0';
	return (s1);
}
