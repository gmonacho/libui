#include <string.h>

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int		i;

	if (!s || !f)
		return ;
	i = 0;
	while (*s && s != NULL)
	{
		f(i, s++);
		i++;
	}
}
